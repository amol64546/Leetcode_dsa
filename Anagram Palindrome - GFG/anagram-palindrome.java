class Sol
{
    int isPossible (String s)
    {
        HashMap<Character,Integer> map = new HashMap<>();
        for(char c: s.toCharArray()){
            map.put(c,map.getOrDefault(c,0)+1);
        }
        
        int count=0;
        for(char c: map.keySet()){
            if(map.get(c)%2!=0){
                count++;
            }
            // odd will be allowed only once
            if(count>1)
                return 0;
        }
        return 1;
    }
}